    private void jButtonEditar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditar1ActionPerformed
        DefaultTableModel clientes = (DefaultTableModel) Clientes.getModel();
        cliente c = new cliente();
        ClienteDAO controle = new ClienteDAO();
        if (Clientes.getSelectedRow() != -1) {
            c.setCod((int) Clientes.getValueAt(Clientes.getSelectedRow(), 0));
            c.setPrimeiro_Nome(nome_cliente.getText());
            c.setSegundo_Nome(sobrenome_cliente.getText());
            c.setUltimo_Nome(ultimo_nome.getText());
            c.setCidade(cidade.getText());
            c.setEstado(estado.getText());
            c.setCPF(cpf.getText());
            c.setRG(rg.getText());
            c.setCNH(cnh_cliente.getText());
            try {
                controle.update(c);
                ultimo_nome.setText("");
                sobrenome_cliente.setText("");
                nome_cliente.setText("");
                cpf.setText("");
                rg.setText("");
                cnh_cliente.setText("");

                listar();
                JOptionPane.showMessageDialog(null, "Atualizado com sucesso!");
            } catch (Exception ex) {
                Logger.getLogger(VerVeiculos.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor Selecione um veículo");
        }
    }//GEN-LAST:event_jButtonEditar1ActionPerformed

