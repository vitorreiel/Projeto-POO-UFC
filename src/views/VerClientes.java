    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        DefaultTableModel clientes = (DefaultTableModel) Clientes.getModel();
        cliente c = new cliente();
        if (Clientes.getSelectedRow() != -1) {
            //String exlcuir=;
            int opçao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o cliente?", "Exclusão", JOptionPane.YES_OPTION);
            if (opçao == JOptionPane.YES_OPTION) {
                c.setCod((int) Clientes.getValueAt(Clientes.getSelectedRow(), 0));
                try {
                    ClienteDAO controle = new ClienteDAO();
                    controle.deletar(c);
                    ultimo_nome.setText("");
                sobrenome_cliente.setText("");
                nome_cliente.setText("");
                cpf.setText("");
                rg.setText("");
                cnh_cliente.setText("");
                    listar();
                    JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                } catch (SQLException ex) {
                    Logger.getLogger(VerVeiculos.class.getName()).log(Level.SEVERE, null, ex);
                } catch (Exception ex) {
                    Logger.getLogger(VerVeiculos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor Selecione um cliente");
        }
    }//GEN-LAST:event_excluirActionPerformed

