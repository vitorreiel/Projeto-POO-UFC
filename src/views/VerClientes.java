    public void listar() throws SQLException, Exception {
        DefaultTableModel clientes = (DefaultTableModel) Clientes.getModel();
        clientes.setNumRows(0);
        ClienteDAO controle = new ClienteDAO();
        for (cliente c : controle.listar()) {
            clientes.addRow(new Object[]{
                c.getCod(),
                c.getPrimeiro_Nome(),
                c.getSegundo_Nome(),
                c.getUltimo_Nome(),
                c.getCidade(),
                c.getEstado(),
                c.getCPF(),
                c.getRG(),
                c.getCNH()
            });
        }

    }

