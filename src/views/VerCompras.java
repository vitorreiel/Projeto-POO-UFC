    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        DefaultTableModel compras = (DefaultTableModel) Compras.getModel();
        compra c = new compra();
        if (Compras.getSelectedRow() != -1) {
            //String exlcuir=;
            int opçao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o histórico dessa compra?", "Exclusão", JOptionPane.YES_OPTION);
            if (opçao == JOptionPane.YES_OPTION) {
                c.setCod((int) Compras.getValueAt(Compras.getSelectedRow(), 0));
                try {
                    CompraDAO controle = new CompraDAO();
                    controle.deletar(c);
                    listar();
                    chassi.setText("");
                forma_pagamento.setText("");
                servicosadd.setText("");
                valor.setText("");
                desconto.setText("");
                    JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                } catch (SQLException ex) {
                    Logger.getLogger(VerVeiculos.class.getName()).log(Level.SEVERE, null, ex);
                } catch (Exception ex) {
                    Logger.getLogger(VerVeiculos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor Selecione um veículo");
        }
    }//GEN-LAST:event_excluirActionPerformed

    private void ComprasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ComprasMouseClicked
        if (Compras.getSelectedRow() != -1) {
            chassi.setText((String) Compras.getValueAt(Compras.getSelectedRow(), 1));
            desconto.setText((String) Compras.getValueAt(Compras.getSelectedRow(), 2));
            forma_pagamento.setText((String) Compras.getValueAt(Compras.getSelectedRow(), 3));
            servicosadd.setText((String) Compras.getValueAt(Compras.getSelectedRow(), 4));
            valor.setText((String) Compras.getValueAt(Compras.getSelectedRow(), 5));
        } else {
            JOptionPane.showMessageDialog(null, "Por favor Selecione um veículo");
        }        
    }//GEN-LAST:event_ComprasMouseClicked

    private void jButtonEditar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditar1ActionPerformed
        DefaultTableModel compras = (DefaultTableModel) Compras.getModel();
        compra c = new compra();
        CompraDAO controle = new CompraDAO();
        if (Compras.getSelectedRow() != -1) {
            c.setCod((int) Compras.getValueAt(Compras.getSelectedRow(), 0));
            c.setChassi(chassi.getText());
            c.setDesconto(desconto.getText());
            c.setForma_Pagamento(forma_pagamento.getText());
            c.setServicosAdicionais(servicosadd.getText());
            c.setValor_Total(valor.getText());
            
            try {
                controle.update(c);
                chassi.setText("");
                forma_pagamento.setText("");
                servicosadd.setText("");
                valor.setText("");
                desconto.setText("");
                listar();
                JOptionPane.showMessageDialog(null, "Atualizado com sucesso!");
            } catch (Exception ex) {
                Logger.getLogger(VerVeiculos.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor Selecione um veículo");
        }
    }//GEN-LAST:event_jButtonEditar1ActionPerformed

