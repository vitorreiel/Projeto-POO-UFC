    public void listar() throws SQLException, Exception {
        DefaultTableModel compras = (DefaultTableModel) Compras.getModel();
        compras.setNumRows(0);
        CompraDAO controle = new CompraDAO();
        for (compra c : controle.listar()) {
            compras.addRow(new Object[]{
                c.getCod(),
                c.getChassi(),
                c.getDesconto(),
                c.getForma_Pagamento(),
                c.getServicosAdicionais(),
                c.getValor_Total()
            
            });
        }
        
    }

