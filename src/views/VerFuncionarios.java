    public void listar() throws SQLException {
        DefaultTableModel funcionarios = (DefaultTableModel) Funcionarios.getModel();
        funcionarios.setNumRows(0);
        FuncionarioDAO controle = new FuncionarioDAO();
        for (funcionario f : controle.listar()) {
            funcionarios.addRow(new Object[]{
                f.getCod(),
                f.getNome(),
                f.getCPF(),
                f.getRG(),
                f.getNumeroPIS()
            });
        }

    }

