    private void jButtonEditar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditar1ActionPerformed
        DefaultTableModel funcionarios = (DefaultTableModel) Funcionarios.getModel();
        funcionario f = new funcionario();
        FuncionarioDAO controle = new FuncionarioDAO();
        if (Funcionarios.getSelectedRow() != -1) {
            f.setCod((int) Funcionarios.getValueAt(Funcionarios.getSelectedRow(), 0));
            f.setNome(nome_funcionario.getText());
            f.setCPF(cpf_funcionario.getText());
            f.setRG(rg_funcionario.getText());
            f.setNumeroPIS(pis_funcionario.getText());
            try {
                controle.update(f);
                nome_funcionario.setText("");
                cpf_funcionario.setText("");
                rg_funcionario.setText("");
                pis_funcionario.setText("");
                listar();
                JOptionPane.showMessageDialog(null, "Atualizado com sucesso!");
            } catch (Exception ex) {
                Logger.getLogger(VerVeiculos.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor Selecione um veículo");
        }
    }//GEN-LAST:event_jButtonEditar1ActionPerformed

    private void FuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_FuncionariosMouseClicked
        if (Funcionarios.getSelectedRow() != -1) {
            nome_funcionario.setText((String) Funcionarios.getValueAt(Funcionarios.getSelectedRow(), 1));
            cpf_funcionario.setText((String) Funcionarios.getValueAt(Funcionarios.getSelectedRow(), 2));
            rg_funcionario.setText((String) Funcionarios.getValueAt(Funcionarios.getSelectedRow(), 3));
            pis_funcionario.setText((String) Funcionarios.getValueAt(Funcionarios.getSelectedRow(), 4));
        } else {
            JOptionPane.showMessageDialog(null, "Por favor Selecione um veículo");
        }
    }//GEN-LAST:event_FuncionariosMouseClicked

    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        DefaultTableModel funcionarios = (DefaultTableModel) Funcionarios.getModel();
        funcionario f = new funcionario();
        if (Funcionarios.getSelectedRow() != -1) {
            //String exlcuir=;
            int opçao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esse funcionário?", "Exclusão", JOptionPane.YES_OPTION);
            if (opçao == JOptionPane.YES_OPTION) {
                f.setCod((int) Funcionarios.getValueAt(Funcionarios.getSelectedRow(), 0));
                try {
                    FuncionarioDAO controle = new FuncionarioDAO();
                    controle.deletar(f);
                    listar();
                    JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                } catch (SQLException ex) {
                    Logger.getLogger(VerVeiculos.class.getName()).log(Level.SEVERE, null, ex);
                } catch (Exception ex) {
                    Logger.getLogger(VerVeiculos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor Selecione um funcionário");
        }
    }//GEN-LAST:event_excluirActionPerformed

