    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   if(senha.getText().equals(csenha.getText())){
        try {
            Connection con=Conexao.getConnection();
            String sql="SELECT * FROM cliente WHERE Login=?";
            PreparedStatement stmt;
            stmt = con.prepareStatement(sql);
            stmt.setString(1,login.getText());
            
            ResultSet r=  stmt.executeQuery();
            

            if(r.next()==false){
            cliente c= new cliente();
            c.setPrimeiro_Nome(nome_cliente.getText());
            c.setSegundo_Nome(sobrenome_cliente.getText());
            c.setUltimo_Nome(ultimo_nome.getText());
            c.setCPF(cpf.getText());
            c.setRG(rg.getText());
            c.setCidade(cidade.getText());
            c.setEstado(estado.getText());
            c.setCNH(cnh_cliente.getText());
            c.setLogin(login.getText());
            c.setSenha(senha.getText());
            ClienteDAO controlar= new ClienteDAO();
            
            try {
                ultimo_nome.setText("");
                sobrenome_cliente.setText("");
                nome_cliente.setText("");
                cpf.setText("");
                rg.setText("");
                cidade.setText("");
                estado.setText("");
                cnh_cliente.setText("");
                login.setText("");
                senha.setText("");
                csenha.setText("");
                controlar.insert(c);
                
            } catch (Exception ex) {
                Logger.getLogger(Cadastrar_Cliente.class.getName()).log(Level.SEVERE, null, ex);
            }}else{  JOptionPane.showMessageDialog(null, "Login ja esxite");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Cadastrar_Cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
   
        }else{
           JOptionPane.showMessageDialog(null, "Senhas Incorretas");
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

